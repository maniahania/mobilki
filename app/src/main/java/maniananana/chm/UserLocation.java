package maniananana.chm;

public class UserLocation {
    private String id;
    private String name;

    public UserLocation() {}

    public String getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public void setId(String id) {
        this.id = id;
    }

    public void setName(String name) {
        this.name = name;
    }
}
